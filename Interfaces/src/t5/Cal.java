package t5;

public class Cal {
    public Cal() {
    }
    public int add(int a,int b) {
        return(a + b);
    }
    public int substract(int a,int b) {
        return(a - b);
    }
    
    public int multiplicar(int a,int b) {
        return(a * b);
    }
    public int dividir(int a,int b) {
        return(a / b);
    }
    public int modulo(int a,int b) {
        return(a % b);
    }
}